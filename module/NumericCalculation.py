
# function to calculate the addition
def calculateAddition(x,y):
    return (x+y)

# function to calculate the difference
def calculateDifference(x,y):
    return (x-y)

# function to calculate the product
def calculateProduct(x,y):
    return (x*y)

# function to calculate the division
def divideNumbers(x,y):
    return (x/y)

# function to calculate the modulus
def calculatemodulus(x,y):
    return (x%y)